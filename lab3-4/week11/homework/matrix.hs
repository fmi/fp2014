-- Размерностите на матрица, представена като двумерен масив
dimensions :: [[a]] -> (Int, Int)

-- Връщаме ред по зададен индекс. Броим от 0.
getRow :: Int -> [[a]] -> [a]

-- Връщаме колона по зададен индекс. Брои от 0.
getCol :: Int -> [[a]] -> [a]

-- Връщаме главния диагонал на матрица
diagonal :: [[a]] -> [a]

-- Транспонираме матрицата
transpose :: [[a]] -> [[a]]

-- Умножаваме две матрици
matrixMultiply :: (Num a) => [[a]] -> [[a]] -> [[a]]
